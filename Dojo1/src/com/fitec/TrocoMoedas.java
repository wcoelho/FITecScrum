package com.fitec;

public class TrocoMoedas
{
    double valorMoeda;
    int quantidadeMoeda;

    public double getValorMoeda()
    {
        return valorMoeda;
    }

    public void setValorMoeda(double valorMoeda)
    {
        this.valorMoeda = valorMoeda;
    }

    public int getQuantidadeMoeda()
    {
        return quantidadeMoeda;
    }

    public void setQuantidadeMoeda(int quantidadeMoeda)
    {
        this.quantidadeMoeda = quantidadeMoeda;
    }

}
